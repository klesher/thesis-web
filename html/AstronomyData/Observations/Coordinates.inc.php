<?php

set_include_path('html/AstronomyData/Observations/');
include_once(get_include_path() . "Angle.inc.php");

class GeoCoordinate {
    private $coordinate = NULL;
    private $hemisphere = NULL;

/**************************************************************************************************/
/*****************************************CONSTRUCTORS*********************************************/
/**************************************************************************************************/
    public function __construct($simpleXML) {
        if ($simpleXML) {
            $this->coordinate = AstronomyData::simpleXMLToObject($simpleXML->Angle, "Angle");
            $this->hemisphere = (string)AstronomyData::simpleXMLToProperty($simpleXML->attributes()->{"Hemisphere"});
        }
    }

/**************************************************************************************************/
/*****************************************FACILITATORS*********************************************/
/**************************************************************************************************/

/**************************************************************************************************/
/*****************************************DATABASE*************************************************/
/**************************************************************************************************/


/**************************************************************************************************/
/******************************************OUTPUT**************************************************/
/**************************************************************************************************/
    public function print($depth) {
        $this->coordinate->print($depth);
        AstronomyData::printProperty("Hemisphere:", $this->hemisphere, $depth);
    }
}
?>
