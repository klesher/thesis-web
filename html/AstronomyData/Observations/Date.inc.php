<?php

class Date {
    private $year = NULL;
    private $month = NULL;
    private $day = NULL;


/**************************************************************************************************/
/*****************************************CONSTRUCTORS*********************************************/
/**************************************************************************************************/
    public function __construct($simpleXML) {
        $this->year = (int)AstronomyData::simpleXMLToProperty($simpleXML->Year);
        $this->month = (int)AstronomyData::simpleXMLToProperty($simpleXML->Month);
        $this->day = (int)AstronomyData::simpleXMLToProperty($simpleXML->Day);
    }

/**************************************************************************************************/
/*****************************************FACILITATORS*********************************************/
/**************************************************************************************************/
    public function isEmpty() {
        return empty($this->year) || empty($this->month) || empty($this->day);
    }

/**************************************************************************************************/
/*****************************************DATABASE*************************************************/
/**************************************************************************************************/


/**************************************************************************************************/
/******************************************OUTPUT**************************************************/
/**************************************************************************************************/
    public function toString() {
        return $this->year . "-" . $this->month . "-" . $this->day;
    }

    public function print($depth) {
        AstronomyData::printProperty("Year:", $this->year, $depth);
        AstronomyData::printProperty("Month:", $this->month, $depth);
        AstronomyData::printProperty("Day:", $this->day, $depth);
    }
}

?>
